#!/usr/bin/env node
// Kernel agent-docs generator (grove BACKLOG B-31).
//
// Builds a regenerable, delimited context block for a consuming project's CLAUDE.md / AGENTS.md,
// so a model authoring against Kernel gets the ownership rules, anti-patterns, CLI reference, and
// component list without a human pasting them. Modeled on Astryx's `init` block; KERNEL namespace.
//
// Zero-dependency: Node built-ins + buildCatalog() (from gen-catalog.mjs) + standard.json. The
// content is deterministic (no timestamp) so re-running is a byte-identical no-op when nothing in
// the sources changed. File writing lives in the `kernel init` CLI command; this module builds
// the block and applies the marker rules.

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { buildCatalog } from './gen-catalog.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const STANDARD_PATH = path.join(ROOT, 'standard.json');

export const START = '<!-- KERNEL:START -->';
export const END = '<!-- KERNEL:END -->';

// ── build the block content (between the markers) ─────────────────────────────────
export function buildAgentBlock() {
  const standard = JSON.parse(fs.readFileSync(STANDARD_PATH, 'utf8'));
  const { components } = buildCatalog();
  const oc = standard.ownershipClasses || {};
  const lint = standard.lint || {};
  const banned = (oc.interactionState?.bannedProps || []).join(', ');
  const synonyms = Object.entries(lint.forbiddenStyleSynonyms || {})
    .map(([k, v]) => `\`${k}\` -> \`${v}\``).join(', ');
  const rawEquivalents = Object.entries(lint.rawControlEquivalents || {})
    .map(([tag, comp]) => `<${tag}> -> ${comp}`).join(', ');

  const byImport = (imp) => components
    .filter((c) => c.import === imp)
    .map((c) => `- \`${c.name}\` - ${c.summary || ''}`)
    .join('\n');

  return [
    START,
    `<!-- Generated by \`kernel init --agents\` (v${standard.version}). Do not hand-edit between these markers; regenerate instead. -->`,
    '',
    '## Working with Kernel (@corilus/kernel)',
    '',
    'Kernel is the Corilus design system: DTCG design tokens + Base UI (headless) component',
    'wrappers, styled through the framework-neutral `.krnl-*` contract. Do not guess its API.',
    '',
    '### Workflow (discover, do not guess)',
    '',
    '1. Read the ownership rules in `@corilus/kernel/standard.json` before writing any markup.',
    '2. Run `kernel component <Name>` (or the MCP `get`) to get a component\'s real props, tokens,',
    '   and usage before you use it. `kernel search <query>` / `kernel build "<idea>"` to discover.',
    '3. Never re-author Base UI interaction state (`data-*`) as props (no `isOpen`, `isChecked`).',
    '4. Run `kernel doctor` after: it checks catalog freshness, the gate, and WCAG contrast.',
    '',
    '### Property ownership (the heart of the Standard)',
    '',
    'Every prop a component exposes falls into exactly one class:',
    '',
    `- **Interaction state** - owned by the Base UI primitive. Styled via \`[data-*]\` in CSS, **never a prop**. Banned prop names: ${banned}.`,
    `- **Pass-through control** - mirror the Base UI prop verbatim (\`checked\`/\`onCheckedChange\`, \`open\`/\`onOpenChange\`, \`disabled\`, \`required\`, \`readOnly\`). Booleans are bare (HTML form), not \`is*\`.`,
    '- **DS presentation** - the only axis Kernel invents: `variant` + `tone` + `size`.',
    '- **Content** - slots / `children` / text props; trailing/leading, not right/left.',
    '',
    '### Anti-patterns (the gate enforces these)',
    '',
    `- No \`is*\` interaction-state props - read state off \`data-*\`.`,
    `- No style synonyms: ${synonyms}.`,
    `- No raw controls where a Kernel wrapper exists: ${rawEquivalents}. Reflect what you use in \`composes\`.`,
    '- `composes` must be honest (list every Kernel part you render).',
    '- `forwardRef` on any component used as a Base UI `render` target.',
    '',
    '### CLI quick-reference',
    '',
    '```',
    'kernel component --list        # every component',
    'kernel component <Name>        # props, composes, usage (+ --dense for context windows)',
    'kernel search <query>          # rank by name/usecases/summary',
    'kernel docs <topic>            # tokens | usage | brands | density | motion',
    'kernel impact <Name>           # what breaks if you change it',
    'kernel build "<idea>"          # closest component kit + a Compose suggestion',
    'kernel doctor                  # freshness + gate + WCAG contrast health',
    '```',
    '',
    'Every command takes `--json` (typed `{type,data}` envelope) and `--dense`. Error codes are',
    'stable and append-only (see `CLI.md`).',
    '',
    '### Components',
    '',
    'Generic surface (`@corilus/kernel`):',
    '',
    byImport('@corilus/kernel'),
    '',
    'Clinical tier (`@corilus/kernel/clinical`, tree-shaken out of external builds):',
    '',
    byImport('@corilus/kernel/clinical'),
    END,
  ].join('\n');
}

// ── apply the marker rules to a file's existing contents (pure) ────────────────────
// existing: current file contents, or null if the file does not exist.
// Returns { contents, action: 'created' | 'refreshed' | 'appended' }.
export function renderDoc(existing, block) {
  if (existing == null) return { contents: block + '\n', action: 'created' };
  const s = existing.indexOf(START);
  const e = existing.indexOf(END);
  if (s !== -1 && e !== -1 && e > s) {
    const before = existing.slice(0, s);
    const after = existing.slice(e + END.length);
    return { contents: before + block + after, action: 'refreshed' };
  }
  const sep = existing.endsWith('\n') ? '\n' : '\n\n';
  return { contents: existing + sep + block + '\n', action: 'appended' };
}

// ── script entry (print the block for inspection) ─────────────────────────────────
if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  process.stdout.write(buildAgentBlock() + '\n');
}
